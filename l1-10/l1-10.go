package main

import (
	"fmt"
	"math"
)

/*
Дана последовательность температурных колебаний: -25.4, -27.0 13.0, 19.0, 15.5, 24.5, -21.0, 32.5.
Объединить данные значения в группы с шагом в 10 градусов.

Последовательность в подмножноствах не важна.

Пример: -20:{-25.0, -27.0, -21.0}, 10:{13.0, 19.0, 15.5}, 20: {24.5}, etc.
*/

func main() {
	// Исходная последовательность температур
	temperatures := []float64{-25.4, -27.0, 13.0, 19.0, 15.5, 24.5, -21.0, 32.5}

	// Карта для группировки температур
	groups := make(map[int][]float64)

	// Проход по всем температурам
	for _, temp := range temperatures {
		// Определяем ключ группы с шагом 10 градусов
		groupKey := int(math.Floor(temp/10) * 10)
		// Добавляем температуру в соответствующую группу
		groups[groupKey] = append(groups[groupKey], temp)
	}

	// Вывод результатов
	for key, values := range groups {
		fmt.Printf("%d: %v\n", key, values)
	}
}

/*
Объяснение кода
Исходные данные: Мы определяем массив temperatures, содержащий заданные температурные значения.
Создание карты: Мы создаём карту groups, где ключ — это целое число, представляющее диапазон температур, а значение — это срез температур, попадающих в этот диапазон.
Цikl по температурам:
Для каждой температуры мы вычисляем groupKey, округляя температуру вниз до ближайшего кратного 10 (используя math.Floor).
Затем мы добавляем температуру в соответствующую группу.
Вывод результата: В конце мы выводим партии температур по установленным группам.

*/
